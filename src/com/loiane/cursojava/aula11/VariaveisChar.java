package com.loiane.cursojava.aula11;

public class VariaveisChar {

	public static void main(String[] args) {
		
		char o = 'o';
		
		char i = 'i';
		
		System.out.println("" + o + i);
		
		
		char O = 111;
		
		char I = 105;
		
		char interrogacao = 0X00E1;
		
		System.out.println("" + O + I + interrogacao);
		
	}

}
